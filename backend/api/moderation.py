"""Модерация: реестр запрещённых слов и бан нарушителей.

ВНИМАНИЕ: список ниже — стартовый набор для детекции опасного/незаконного
контента (терроризм, наркоторговля, торговля оружием, CSAM). Его НУЖНО
аккуратно дополнять и проверять под свою аудиторию: слишком широкие слова
дают ложные срабатывания, а авто-бан необратим. Сопоставление идёт по
границам слов и в нижнем регистре.
"""
import re

# Категории запрещённого. Перечисляем основы слов/фразы (рус + англ).
BANNED_TERMS = {
    'терроризм': [
        'теракт', 'теракта', 'террорист', 'терроризм', 'взрывчатк', 'смертник',
        'шахид', 'игил', 'isis', 'аль-каида', 'al-qaeda', 'устрою взрыв', 'заложу бомбу',
        'взорву', 'детонатор', 'пояс смертника', 'suicide bomb', 'terror attack',
    ],
    'наркотики': [
        'наркотик', 'наркоторг', 'наркосбыт', 'закладк', 'мефедрон', 'амфетамин',
        'метамфетамин', 'героин', 'кокаин', 'гашиш', 'марихуан', 'гидропон',
        'продам наркотик', 'куплю наркотик', 'cocaine', 'heroin', 'mdma',
        'methamphetamine', 'drug deal',
    ],
    'оружие': [
        'торговля оружием', 'куплю оружие', 'продам оружие', 'купить ствол',
        'купить взрывчатку', 'нелегальное оружие', 'illegal weapon', 'buy a gun illegally',
    ],
    'csam': [
        'детское порно', 'детская порнография', 'child porn', 'cp video', 'чайлд порн',
    ],
}


def _build_regex(terms):
    """Регэксп с границами слов, корректно работающий и с кириллицей."""
    escaped = sorted((re.escape(t) for t in terms), key=len, reverse=True)
    body = '|'.join(escaped)
    # граница: слева/справа не буква/цифра (учитываем кириллицу)
    return re.compile(r'(?<![\w])(?:' + body + r')(?![\w])', re.IGNORECASE | re.UNICODE)


_ALL = []
for _cat, _list in BANNED_TERMS.items():
    _ALL.append((_cat, _build_regex(_list)))


def find_banned(text):
    """Возвращает (категория, совпавший_фрагмент) или None."""
    if not text:
        return None
    for cat, rx in _ALL:
        m = rx.search(text)
        if m:
            return (cat, m.group(0))
    return None


def get_client_ip(request):
    xff = request.META.get('HTTP_X_FORWARDED_FOR', '')
    if xff:
        return xff.split(',')[0].strip()
    return request.META.get('REMOTE_ADDR', '') or ''
